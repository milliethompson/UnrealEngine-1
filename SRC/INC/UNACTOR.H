// If this actor is a player, return it as a Pawn.
// Otherwise return NULL.
inline APawn *AActorBase::GetPlayer() const
{
	guardSlow(AActorBase::GetPlayer);

	// Only descendents of the pawn class may be players.
	if( !IsKindOf("Pawn") )
		return NULL;

	// Players must have cameras.
	if( ((APawn*)this)->Camera == NULL )
		return NULL;

	// This is a player.
	return (APawn*)this;

	unguardSlow;
}

// Determine if BlockingActor should block actors of the given class.
inline BOOL AActorBase::IsBlockedBy( const AActor &Other ) const
{
	guardSlow(AActorBase::IsBlockedBy);

	// Players and nonplayers are handled separately.
	if( GetPlayer () )
	{
		// Return whether the other actor blocks players.
		return bBlocksActors && Other.bBlocksPlayers;
	}
	else
	{
		// See if either actor doesn't block any other actors.
		if( !bBlocksActors || !Other.bBlocksActors )
			return 0;

		// If there are no specific blocked/passed classes everything is blocked.
		if( Other.BlockedClasses[0]==0 && Other.PassedClasses[0]==0 )
			return 1;

		// See if this class is explicitly allowed to pass.
		for( int i=0; i<3; i++ )
		{
			if( Other.PassedClasses[i] && IsKindOf(Other.PassedClasses[i]) )
				return 0;
		}

		// See if this class is explicitly blocked.
		for( i=0; i<3; i++ )
		{
			if( Other.BlockedClasses[i] && IsKindOf(Other.BlockedClasses[i]) )
				return 1;
		}

		// This actor is not blocked.
		return 0;
	}
	unguardSlow;
}

