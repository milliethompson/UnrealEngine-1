//
// See if this actor belongs to the specified class.
//
inline BOOL AActorBase::IsKindOf( const class UClass *SomeParent ) const
{
	guardSlow(AActorBase::IsKindOf);
	for( UClass *TempClass=Class; TempClass; TempClass=TempClass->ParentClass )
		if( TempClass==SomeParent )
			return 1;
	return 0;
	unguardSlow;
}

//
// See if this actor belongs to the specified class.
//
inline BOOL AActorBase::IsKindOf( const char *ClassName ) const
{
	guardSlow(AActorBase::IsKindOf);
	for( UClass *TempClass=Class; TempClass; TempClass=TempClass->ParentClass )
		if( stricmp( TempClass->Name, ClassName ) == 0 )
			return 1;
	return 0;
	unguard;
}

//
// Get the pointer to a particular actor property.
//
inline void *AActorBase::GetPropertyPtr( int iProperty,int iElement )
{
	guardSlow(AActorBase::GetPropertyPtr);
	FClassProperty &Property = Class->Element(iProperty);
	return &((AActor*)this)->Private.PropertyBins[Property.Bin]
		[
		Property.Offset + iElement * Property.ElementSize
		];
	unguardSlow;
}
